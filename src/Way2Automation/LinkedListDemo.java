package Way2Automation;

import java.util.LinkedList;

public class LinkedListDemo {

	public static void main(String[] args) {
		LinkedList<Integer> list = new LinkedList<Integer>();
		list.add(12);
		list.add(6);
		list.add(23);
		list.addFirst(120);
		list.addLast(145);
		System.out.println(list);
		

	}

}
