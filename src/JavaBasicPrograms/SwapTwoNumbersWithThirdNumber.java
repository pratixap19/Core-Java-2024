package JavaBasicPrograms;

public class SwapTwoNumbersWithThirdNumber {

	public static void main(String[] args) {
		
		int n1=20;
		int n2=30;
		int temp;
		System.out.println("Value before swap: " + n1 + "," + n2);
		temp=n1;
		n1=n2;
		n2=temp;
		System.out.println("Value after swap: " + n1 + "," + n2);
		
	}

}
